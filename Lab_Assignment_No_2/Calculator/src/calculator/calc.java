    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        
        // Code for = Button
        
        if(t.getText().isEmpty()|| num1==Float.MAX_VALUE || choice==-1){t.grabFocus();return;}
        
        num2=Float.parseFloat(t.getText());
        
        switch(choice){
            
            case 1 : 
                t.setText(Float.toString(num1+num2));
                break;
                
            case 2 : 
                t.setText(Float.toString(num1-num2));
                break;
                
            case 3 : 
                t.setText(Float.toString(num1*num2));
                break;
                
            case 4 : 
                t.setText(Float.toString(num1/num2));
                break;
                
            case 5 : 
                t.setText(Integer.toString( (int)num1% (int)num2));
                break;
        }
        
        num1=Float.MAX_VALUE;
        num2=Float.MAX_VALUE;
        
    }//GEN-LAST:event_jButton14ActionPerformed

